package frost.util.gui.treetable;
import javax.swing.tree.*;
 
/**
 * @version 1.2 10/27/98
 * An abstract implementation of the TreeTableModel interface, 
 * handling the list of listeners. 
 * @author Philip Milne
 */

public abstract class AbstractTreeTableModel extends DefaultTreeModel implements TreeTableModel {

    public AbstractTreeTableModel(TreeNode root) {
        super(root);
    }

    //
    // Default implementations for methods in the TreeTableModel interface. 
    //

    public Class getColumnClass(int column) { return Object.class; }

   /** By default, make the column with the Tree in it the only editable one. 
    *  Making this column editable causes the JTable to forward mouse 
    *  and keyboard events in the Tree column to the underlying JTree. 
    */ 
    public boolean isCellEditable(Object node, int column) { 
//        return false;
        return getColumnClass(column) == TreeTableModel.class; 
    }

    public void setValueAt(Object aValue, Object node, int column) {}
}
